    public void loadData(){

    MahasiswaService service = new service.MahasiswaService();
    List<Mahasiswa> list = service.getAll();

    DefaultTableModel model = (DefaultTableModel) tblMahasiswa.getModel();
    model.setRowCount(0);

    for(Mahasiswa m : list){

        Object[] row = {
            m.getNim(),
            m.getName(),
            m.getAge(),
            m.getJurusan()
        };

        model.addRow(row);
    }

}

