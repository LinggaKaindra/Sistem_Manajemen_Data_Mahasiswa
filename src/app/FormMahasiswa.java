    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
try {
        // Ambil data dari form
        String nim = txtNim.getText();
        String nama = txtNama.getText();
        String jurusan = txtJurusan.getText();
        
        // Validasi jika umur kosong
        if(txtUmur.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Umur harus diisi!");
            return;
        }
        int umur = Integer.parseInt(txtUmur.getText());

        // Buat objek Mahasiswa baru
        Mahasiswa m = new Mahasiswa(nim, nama, umur, jurusan);

        // Jalankan service
        MahasiswaService service = new MahasiswaService();
        service.update(m);

        JOptionPane.showMessageDialog(this, "Data Mahasiswa Berhasil Diperbarui!");
        
        loadData();  // Refresh tabel agar muncul data baru
        resetForm(); // NIM akan jadi Editable(true) lagi di sini
        
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Umur harus berupa angka!");
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + e.getMessage());
    }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
    String nim = txtNim.getText();

        if (nim.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Pilih data yang akan dihapus!");
            return;
        }

        int konfirmasi = JOptionPane.showConfirmDialog(this, 
                "Yakin ingin menghapus mahasiswa dengan NIM " + nim + "?", 
                "Konfirmasi Hapus", JOptionPane.YES_NO_OPTION);

        if (konfirmasi == JOptionPane.YES_OPTION) {
            new MahasiswaService().delete(nim);
            JOptionPane.showMessageDialog(this, "Data Berhasil Dihapus");
            loadData();
            resetForm();
        }
    }//GEN-LAST:event_btnHapusActionPerformed

    private void tblMahasiswaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblMahasiswaMouseClicked
int row = tblMahasiswa.getSelectedRow();
        if (row != -1) {
            txtNim.setText(tblMahasiswa.getValueAt(row, 0).toString());
            txtNama.setText(tblMahasiswa.getValueAt(row, 1).toString());
            txtUmur.setText(tblMahasiswa.getValueAt(row, 2).toString());
            txtJurusan.setText(tblMahasiswa.getValueAt(row, 3).toString());
            
            // NIM biasanya Primary Key, sebaiknya tidak diubah saat Update
            txtNim.setEditable(false); 
        }
    }//GEN-LAST:event_tblMahasiswaMouseClicked

